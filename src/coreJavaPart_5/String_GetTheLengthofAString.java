package coreJavaPart_5;

public class String_GetTheLengthofAString {
    public static void main(String[] args) {
        //Declare the String variables
        String text ="Hello World! ";
        System.out.println("The text is: "+ text);

        //Get the length fo the string and store in the integer variable
        int length = text.length();

        //Print the length of the variable
        System.out.println("The length of the text: "+ length);




    }
}
